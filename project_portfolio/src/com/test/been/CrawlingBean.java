package com.test.been;

public class CrawlingBean {
	private int c_index;
	private String c_title; 
	private String c_ranking; 
	private String c_date ;
	private String c_img;
	
	
	
	public int getC_index() {
		return c_index;
	}
	public void setC_index(int c_index) {
		this.c_index = c_index;
	}
	public String getC_title() {
		return c_title;
	}
	public void setC_title(String c_title) {
		this.c_title = c_title;
	}
	public String getC_ranking() {
		return c_ranking;
	}
	public void setC_ranking(String c_ranking) {
		this.c_ranking = c_ranking;
	}
	public String getC_date() {
		return c_date;
	}
	public void setC_date(String c_date) {
		this.c_date = c_date;
	}
	public String getC_img() {
		return c_img;
	}
	public void setC_img(String c_img) {
		this.c_img = c_img;
	}
	
	
}
